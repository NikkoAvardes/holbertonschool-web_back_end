#!/usr/bin/env python3
"""
Ce module fournit une fonction pour convertir un nombre flottant en chaîne.
"""


def to_str(n: float) -> str:
    """
    Convertit un nombre flottant en chaîne de caractères.

    Args:
        n (float): Le nombre à convertir.

    Returns:
        str: La représentation en chaîne du nombre.
    """
    return str(n)
