#!/usr/bin/env python3
"""
Ce module fournit une fonction pour concaténer deux chaînes de caractères.
"""


def concat(a: str, b: str) -> str:
    """
    Concatène deux chaînes de caractères et retourne le résultat.

    Args:
        a (str): La première chaîne à concaténer.
        b (str): La seconde chaîne à concaténer.

    Returns:
        str: La chaîne concaténée.
    """
    return a + b
