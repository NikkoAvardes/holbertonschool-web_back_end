#!/usr/bin/env python3
"""
Module 0-add.py : Fournit une fonction pour additionner deux nombres flottants.
"""


def add(a: float, b: float) -> float:
    """
    Additionne deux nombres flottants et retourne le résultat.
    Args:
        a (float): Le premier nombre à additionner.
        b (float): Le second nombre à additionner.
    Returns:
        float: La somme des deux nombres.
    """
    return a + b
